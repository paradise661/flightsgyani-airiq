    {
        return $this->belongsTo('App\Models\User', 'user_id');
    }

    public function getTickets()
    {
        return $this->hasMany('App\Models\InternationalFlight\FlightTicket', 'flight_booking_id');
    }

    public function getPaidAmountAttribute()
    {
        if ($this->payments()->get()->count() > 0) {
            $totalAmount = 0;
            foreach ($this->payments()->get() as $payment) {
                $totalAmount += $payment->amount;
            }
            return $totalAmount;
        } else {
            return 0;
        }
    }

    public function payments()
    {
        return $this->morphMany('App\Models\Finance\Payment', 'booking');
    }

