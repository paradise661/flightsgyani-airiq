    public function getAdultsAttribute()
    {
        return $this->bookingDetails()->where('pax_type', 'ADT')->count();
    }

    public function bookingDetails()
    {
        return $this->hasMany('App\Models\InternationalFlight\FlightBookingDetail', 'flight_booking_id');
    }

    public function getChildsAttribute()
    {
        return $this->bookingDetails()->where('pax_type', 'CHLD')->count();
    }

    public function getInfantsAttribute()
    {
        return $this->bookingDetails()->where('pax_type', 'INFT')->count();
    }

